package ac.th.sau;

public class MyClass01 {
    //Encapsulation
    private int dataA;
    private String dataB;

    //ให้ใฃ้งาน Data ผ่าน Method Getter/Setter

    public int getDataA() {
        return dataA;
    }

    public void setDataA(int dataA) {
        this.dataA = dataA;
    }

    public String getDataB() {
        return dataB;
    }

    public void setDataB(String dataB) {
        this.dataB = dataB;
    }
}
